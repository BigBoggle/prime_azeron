bool oled_task_user(void)
{

    oled_set_cursor(0, 6);
    render_animation();
    return false;
}

void oled_display_shutdown_message(bool bootloader)
{
    oled_clear();
    oled_set_cursor(0, 4); // Centered for better visibility
    if (bootloader)
    {
        oled_write_P(PSTR("Awaiting Firmware..."), false);
    }
    else
    {
        oled_write_P(PSTR("Rebooting..."), false);
    }
    oled_render();
}

bool shutdown_user(bool jump_to_bootloader)
{
    oled_display_shutdown_message(jump_to_bootloader);
    wait_ms(1000);    // Give some time for the message to be seen
    reset_keyboard(); // Perform actual reboot or bootloader jump
    return true;      // Indicate shutdown completed
}

